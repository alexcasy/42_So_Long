#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	number[11];
	int		i;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	i = 0;
	while (n > 9)
	{
		number[i++] = '0' + n % 10;
		n /= 10;
	}
	number[i] = '0' + n;
	while (i >= 0)
		write(fd, &number[i--], 1);
}
